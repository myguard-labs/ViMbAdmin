/**
 * Class to store and retrieve the version of ViMbAdmin.
 *
 * @package    ViMbAdmin
 * @subpackage Library
 * @copyright  Copyright (c) 2011 Open Source Solutions Limited
 * @license    http://opensource.org/licenses/gpl-3.0.html GNU General Public License, version 3 (GPLv3)
 */
final class ViMbAdmin_Version
{
    /**
     * Version identification - see compareVersion().
     *
     * This is the single source of truth for the application version: the
     * footer and the Maintenance tab both read VERSION directly. Release
     * workflow:
     *   1. bump VERSION here (e.g. 4.0.0-rc1 -> 4.0.0-rc2 -> 4.0.0),
     *      and MILESTONE on a minor/major change,
     *   2. tag the commit: `git tag v4.0.0-rc1 && git push --tags`.
     * DBVERSION is independent — bump it ONLY when the schema (entity mappings)
     * changes, not on every release.
     */
    const VERSION = '4.0.0-rc1';

    /**
     * Version milestone
     *
     * The version milestone is used to publicly identify the running version
     * and should therefore not include the patch level.
     */
    const MILESTONE = '4.0';

    /**
     * Database schema version
     */
    const DBVERSION = 5;

    /**
     * Database schema version name
     *
     * v3 — single consolidated fork schema step above upstream's v2 ("Earth"):
     * covers the mailbox-task queue (mailbox_task), admin last_login, the MCP
     * token table incl. allowed_domains, the Dovecot quota-clone table
     * (dovecot_quota) and the Dovecot last-login table (dovecot_last_login).
     * Doctrine SchemaTool applies the whole diff in one pass on a fresh DB.
     *
     * v4 — ON DELETE CASCADE FKs on the two Dovecot-owned, read-only,
     * unassociated tables (dovecot_quota / dovecot_last_login) → mailbox
     * (username), plus a collation alignment of dovecot_quota.username. These
     * can't be expressed by the schema-tool (no Doctrine association — the
     * Mailbox PK is `id`, not `username`), so ViMbAdmin_Schema::extraSql()
     * adds them, introspection-guarded. Deleting a mailbox now cascade-deletes
     * its quota + last-login rows at the DB layer. Mirror:
     * contrib/migrations/2026-06-quota-lastlogin-fk-cascade.sql.
     *
     * v5 — archive.autoprune boolean column. Queue ARCHIVE/DELETE backups now
     * write archive rows; DELETE-with-backup marks autoprune=1 so the backup is
     * pruned automatically after queue.autoprune.days (application.ini). Plain
     * additive column, generated by the schema-tool.
     */
    const DBVERSION_NAME = 'Archive autoprune column + queue-backed archives';

    /**
     * The latest stable version Zend Framework available
     *
     * @var string
     */
    protected static $_lastestVersion = null;

    /**
     * Compare the specified version string $version
     * with the current ViMbAdmin_Version::VERSION.
     *
     * @param  string  $version  A version string (e.g. "0.7.1").
     * @return int           -1 if the $version is older,
     *                           0 if they are the same,
     *                           and +1 if $version is newer.
     *
     */
    public static function compareVersion( $version )
    {
        return version_compare( $version, self::VERSION );
    }

    /**
     * Fetches the version of the latest stable release
     *
     * @link http://framework.zend.com/download/latest
     * @return string
     */
    public static function getLatest()
    {
        if( null === self::$_lastestVersion )
        {
            self::$_lastestVersion = 'not available';

            $handle = fopen( 'http://www.opensolutions.ie/open-source/vimbadmin/latest-v3', 'r' );
            if( $handle !== false )
            {
                self::$_lastestVersion = preg_replace( "/[^0-9\.]/", "", trim( stream_get_contents( $handle, 12 ) ) );
                fclose( $handle );
            }
        }

        return self::$_lastestVersion;
    }
}
